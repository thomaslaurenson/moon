func TestResolveErrors(t *testing.T) {
	t.Parallel()
	e := New(testFS())

	tests := []struct {
		name    string
		bundle  string
		wantErr error
	}{
		{name: "unknown bundle", bundle: "nope", wantErr: ErrUnknownBundle},
		{name: "include cycle", bundle: "cyc-a", wantErr: ErrIncludeCycle},
	}
	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()
			_, err := e.Expand(tc.bundle)
			if !errors.Is(err, tc.wantErr) {
				t.Fatalf("Expand(%q) error = %v, want errors.Is(_, %v)", tc.bundle, err, tc.wantErr)
			}
		})
	}
}

